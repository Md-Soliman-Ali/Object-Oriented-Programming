package Encapsulation;

public class AccountOwner {
	
	private int id;
	private String name;
	private int amounts;
	
	public int getId() 
	{
		return id;
	}
	
	public void setId(int id)
	{
		this.id = id;
	}
	
	public String getName() 
	{
		return name;
	}
	
	public void setName(String name) 
	{
		this.name = name;
	}
	
	public int getAmounts() 
	{
		return amounts;
	}
	
	public void setAmounts(int amounts) 
	{
		this.amounts = amounts;
	}

}
